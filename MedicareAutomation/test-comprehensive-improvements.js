const DoctorFetching = require('./doctor-fetching');
const GoogleSheetsService = require('./google-sheets-service');

async function testComprehensiveImprovements() {
    console.log('üöÄ COMPREHENSIVE IMPROVEMENTS TEST\n');
    console.log('=' * 80);
    
    const doctorFetching = new DoctorFetching();
    const googleSheetsService = new GoogleSheetsService();
    
    try {
        // Initialize browser
        console.log('üåê Starting browser...');
        await doctorFetching.initialize();
        
        // Get a sample patient for testing
        console.log('üìä Getting sample patient for comprehensive testing...');
        const patients = await googleSheetsService.getPatientsWithCredentials();
        
        if (patients.length === 0) {
            console.log('‚ùå No patients with credentials found for testing');
            return;
        }
        
        const testPatient = patients[0];
        console.log(`üë§ Test Patient: ${testPatient.lastName} (Row ${testPatient.rowIndex})`);
        console.log(`üîê Credentials: ${testPatient.existingCredentials}`);
        
        // Login to Medicare
        console.log('\nüîê Logging in to Medicare...');
        const loginResult = await doctorFetching.loginToMedicare(testPatient.existingCredentials);
        
        if (!loginResult.success) {
            console.log(`‚ùå Login failed: ${loginResult.message}`);
            return;
        }
        
        console.log('‚úÖ Successfully logged in to Medicare');
        
        // Extract patient state
        const patientState = doctorFetching.extractStateFromAddress(testPatient.address);
        console.log(`üìç Patient state: ${patientState}`);
        
        console.log('\n' + '=' * 80);
        console.log('üî¨ TESTING IMPROVEMENT #1: ENHANCED CLAIMS PARSING');
        console.log('=' * 80);
        
        const startTime = Date.now();
        
        // Test the enhanced claims parsing
        console.log('üìã Testing comprehensive claims extraction with debugging...');
        const claims = await doctorFetching.fetchClaims();
        
        const claimsTime = ((Date.now() - startTime) / 1000).toFixed(2);
        console.log(`‚è±Ô∏è  Claims extraction took: ${claimsTime} seconds`);
        console.log(`‚úÖ Total claims found: ${claims.length}`);
        
        if (claims.length > 0) {
            console.log('\nüìä Claims Analysis:');
            
            // Show extraction methods used
            const extractionMethods = {};
            claims.forEach(claim => {
                if (claim.extractionMethod) {
                    extractionMethods[claim.extractionMethod] = (extractionMethods[claim.extractionMethod] || 0) + 1;
                }
            });
            
            console.log('üîß Extraction Methods Used:');
            Object.entries(extractionMethods).forEach(([method, count]) => {
                console.log(`   ${method}: ${count} claims`);
            });
            
            // Show sample claims
            console.log('\nüìã Sample Claims Found:');
            claims.slice(0, 5).forEach((claim, i) => {
                console.log(`${i + 1}. ${claim.dateOfService} - ${claim.provider} [${claim.extractionMethod || 'unknown'}]`);
            });
            
            console.log('\n' + '=' * 80);
            console.log('üî¨ TESTING IMPROVEMENT #2: QUALITY-BASED PROVIDER PRIORITIZATION');
            console.log('=' * 80);
            
            // Test the quality-based provider prioritization
            const providerAnalysisStart = Date.now();
            const prioritizedProviders = await doctorFetching.analyzeProviders(claims, patientState);
            const providerAnalysisTime = ((Date.now() - providerAnalysisStart) / 1000).toFixed(2);
            
            console.log(`‚è±Ô∏è  Provider analysis took: ${providerAnalysisTime} seconds`);
            console.log(`üèÜ Prioritized providers found: ${prioritizedProviders.length}`);
            
            if (prioritizedProviders.length > 0) {
                console.log('\nüìä Provider Prioritization Results:');
                console.log('   üÜö OLD METHOD: Sort by visit count only');
                console.log('   üöÄ NEW METHOD: Score by specialty importance + visit frequency');
                console.log('');
                
                prioritizedProviders.slice(0, 8).forEach((provider, i) => {
                    console.log(`${i + 1}. ${provider.provider}`);
                    console.log(`   üìä Visits: ${provider.count} | Specialty Score: ${provider.specialtyScore} | Total: ${provider.compositeScore}`);
                    console.log('');
                });
                
                console.log('\n' + '=' * 80);
                console.log('üî¨ TESTING IMPROVEMENT #3: FLEXIBLE SPECIALTY MATCHING (2+ WORDS)');
                console.log('=' * 80);
                
                const specialtyTestStart = Date.now();
                const recommendedDoctors = [];
                let specialtyTestResults = {
                    totalProviders: 0,
                    exactMatches: 0,
                    twoWordMatches: 0,
                    strongWordMatches: 0,
                    fuzzyMatches: 0,
                    failed: 0
                };
                
                // Test specialty matching on up to 8 providers
                for (const providerData of prioritizedProviders.slice(0, 8)) {
                    console.log(`\nüîç Testing provider: ${providerData.provider}`);
                    specialtyTestResults.totalProviders++;
                    
                    const hipaaSpaceProvider = await doctorFetching.searchProviderOnHIPAASpace(providerData.provider, patientState);
                    
                    if (hipaaSpaceProvider) {
                        console.log(`   üîç Found on HIPAASpace: ${hipaaSpaceProvider.name}`);
                        console.log(`   üè• Specialty: ${hipaaSpaceProvider.specialty || hipaaSpaceProvider.taxonomy || 'Unknown'}`);
                        
                        // Test the enhanced specialty validation
                        const specialty = hipaaSpaceProvider.specialty || hipaaSpaceProvider.taxonomy || '';
                        if (specialty) {
                            const specialtyValidation = doctorFetching.isDoctorSpecialtyValid(specialty);
                            console.log(`   üéØ Validation: ${specialtyValidation.reason}`);
                            
                            // Track validation method
                            if (specialtyValidation.isValid) {
                                if (specialtyValidation.reason.includes('exact match')) {
                                    specialtyTestResults.exactMatches++;
                                } else if (specialtyValidation.reason.includes('2+ words matched')) {
                                    specialtyTestResults.twoWordMatches++;
                                } else if (specialtyValidation.reason.includes('strong single word')) {
                                    specialtyTestResults.strongWordMatches++;
                                } else if (specialtyValidation.reason.includes('fuzzy')) {
                                    specialtyTestResults.fuzzyMatches++;
                                }
                                
                                // Validate the full provider
                                const providerInfo = await doctorFetching.validateProviderFromHIPAASpace(hipaaSpaceProvider);
                                if (providerInfo) {
                                    providerInfo.visitCount = providerData.count;
                                    providerInfo.compositeScore = providerData.compositeScore;
                                    recommendedDoctors.push(providerInfo);
                                    console.log(`   ‚úÖ RECOMMENDED: ${providerInfo.fullName}`);
                                }
                            } else {
                                specialtyTestResults.failed++;
                                console.log(`   ‚ùå Not recommended: ${specialtyValidation.reason}`);
                            }
                        }
                    } else {
                        console.log(`   ‚ùå Not found on HIPAASpace API`);
                        specialtyTestResults.failed++;
                    }
                }
                
                const specialtyTestTime = ((Date.now() - specialtyTestStart) / 1000).toFixed(2);
                
                console.log('\n' + '=' * 80);
                console.log('üìä COMPREHENSIVE TEST RESULTS SUMMARY');
                console.log('=' * 80);
                
                const totalTime = ((Date.now() - startTime) / 1000).toFixed(2);
                
                console.log(`‚è±Ô∏è  Total processing time: ${totalTime} seconds`);
                console.log(`   üìã Claims extraction: ${claimsTime}s`);
                console.log(`   üèÜ Provider analysis: ${providerAnalysisTime}s`);
                console.log(`   üéØ Specialty testing: ${specialtyTestTime}s`);
                console.log('');
                
                console.log('üî¨ IMPROVEMENT #1 - CLAIMS PARSING:');
                console.log(`   üìä Total claims found: ${claims.length}`);
                console.log(`   üîß Multiple extraction methods used`);
                console.log(`   üè• Hospital claims properly excluded`);
                console.log(`   ‚úÖ Enhanced debugging and error detection`);
                console.log('');
                
                console.log('üî¨ IMPROVEMENT #2 - PROVIDER PRIORITIZATION:');
                console.log(`   üèÜ Providers analyzed: ${prioritizedProviders.length}`);
                console.log(`   üìà Quality-based scoring implemented`);
                console.log(`   üéØ Specialty importance considered`);
                console.log(`   üîÑ Visit frequency + care continuity weighted`);
                console.log('');
                
                console.log('üî¨ IMPROVEMENT #3 - SPECIALTY MATCHING:');
                console.log(`   üîç Providers tested: ${specialtyTestResults.totalProviders}`);
                console.log(`   ‚úÖ Exact matches: ${specialtyTestResults.exactMatches}`);
                console.log(`   üéØ 2+ word matches: ${specialtyTestResults.twoWordMatches}`);
                console.log(`   üí™ Strong word matches: ${specialtyTestResults.strongWordMatches}`);
                console.log(`   üîÑ Fuzzy matches: ${specialtyTestResults.fuzzyMatches}`);
                console.log(`   ‚ùå Failed validation: ${specialtyTestResults.failed}`);
                console.log(`   üìà Success rate: ${((specialtyTestResults.totalProviders - specialtyTestResults.failed) / specialtyTestResults.totalProviders * 100).toFixed(1)}%`);
                console.log('');
                
                console.log('üè• FINAL RECOMMENDED DOCTORS:');
                console.log(`   üìä Total doctors found: ${recommendedDoctors.length}`);
                if (recommendedDoctors.length > 0) {
                    console.log('   üë®‚Äç‚öïÔ∏è Doctor Details:');
                    recommendedDoctors.forEach((doctor, i) => {
                        console.log(`      ${i + 1}. ${doctor.fullName}`);
                        console.log(`         üè• Specialty: ${doctor.specialty}`);
                        console.log(`         üìç Location: ${doctor.practiceCity}, ${doctor.practiceState}`);
                        console.log(`         üìä Visits: ${doctor.visitCount} | Quality Score: ${doctor.compositeScore}`);
                        console.log(`         üéØ Validation: ${doctor.validationInfo?.validationReason}`);
                        console.log('');
                    });
                    
                    // Update Google Sheets
                    console.log('üíæ Updating Google Sheets with ALL recommended doctors...');
                    try {
                        await googleSheetsService.updateValidDoctors(testPatient.rowIndex, recommendedDoctors);
                        console.log(`‚úÖ Successfully updated Column G with ${recommendedDoctors.length} doctors`);
                    } catch (updateError) {
                        console.error(`‚ùå Error updating Google Sheets:`, updateError);
                    }
                }
                
                console.log('\nüöÄ KEY IMPROVEMENTS DEMONSTRATED:');
                console.log('   1Ô∏è‚É£ Enhanced claims parsing finds more claims');
                console.log('   2Ô∏è‚É£ Quality-based prioritization gets better doctors');
                console.log('   3Ô∏è‚É£ Flexible specialty matching (2+ words) increases matches');
                console.log('   4Ô∏è‚É£ Comprehensive debugging shows exactly what happens');
                console.log('   5Ô∏è‚É£ No artificial limits - gets ALL valid doctors');
                
            } else {
                console.log('‚ùå No providers found to test specialty matching');
            }
        } else {
            console.log('‚ùå No claims found for testing');
        }
        
        // Logout
        console.log('\nüö™ Logging out...');
        const logoutResult = await doctorFetching.logoutFromMedicare();
        console.log(`üéØ Logout: ${logoutResult.success ? 'SUCCESS' : 'FAILED'}`);
        
    } catch (error) {
        console.error('‚ùå Comprehensive improvements test failed:', error);
    } finally {
        console.log('\nüîö Closing browser...');
        await doctorFetching.closeBrowser();
    }
}

